package model;

public class Card {
	int id;
	Territory territory;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Territory getTerritory() {
		return territory;
	}

	public void setTerritory(Territory territory) {
		this.territory = territory;
	}

}
